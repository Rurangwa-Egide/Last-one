const priductContainer = document.querySelector(".products")
const serchInput = document.querySelector(".search")
const categoriesContainer = document.querySelector(".cats")


const displayProducts = (filteredProducts) =>{
    priductContainer.innerHTML = filteredProducts.map(
        (product)=>

    `
               <div class="product">
                        <img src=${product.img}
                         alt="">
                        <span class="name">${product.name}</span>
                        <a href="watch.html"><span class="priceText">$${product.price}</span></a>
                    </div>
    
    `
).join("");

};
        displayProducts(data)
         serchInput.addEventListener("keyup",(e)=>{
            const value = e.target.value.toLowerCase();

            if(value){
                displayProducts(data.filter(item=> item.name.toLowerCase().indexOf(value) !== -1))
            }
            else{
                displayProducts(data)
            }

 });
 const setCategories = () =>{
    const allCats = data.map(item=>item.Cat)
    const categories =["All",...allCats.filter((item,i)=>{
        return allCats.indexOf(item) ===i
    }),
];
categoriesContainer.innerHTML = categories.map(cat =>
    `
    <span class="cat">${cat}</span>
    
    `
).join("")

categoriesContainer.addEventListener("click",(e) =>{
    const selectedCat = e.target.textContent;
    selectedCat === "All" ? displayProducts(data) : displayProducts(data.filter((item) => item.Cat === selectedCat)) 
})
    
 }
